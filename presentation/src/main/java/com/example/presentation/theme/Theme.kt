@Immutable
data class TextColor(
    val primaryColor: Color,
    val primaryColorInverse: Color
)

val LocalTextColors = staticCompositionLocalOf {
    TextColor(
        primaryColor = Color.Unspecified,
        primaryColorInverse = Color.Unspecified
    )
}

@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme =
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.S) {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        } else {
            if (darkTheme) darkColors else lightColors
        }

    val textColors = TextColor(
        primaryColor = Color.Black,
        primaryColorInverse = Color.White
    )

    CompositionLocalProvider(LocalTextColors provides textColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            content = content
        )
    }
}

object AppTheme {
    val textColors: TextColor
        @Composable
        get() = LocalTextColors.current
}